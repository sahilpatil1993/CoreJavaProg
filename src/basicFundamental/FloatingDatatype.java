package basicFundamental;

public class FloatingDatatype 
{
	
	public static void main(String[] args)
	{

		float firstNum = 20f;
		float secondNum = 7f;
		
		float div = firstNum/secondNum;
	
		System.out.println(firstNum);
		System.out.println(div);
		
		double thirdNum = 21.26;
		double fourthNum = 3.15;
		
		double div1 = thirdNum/fourthNum;
		
		System.out.println(div1);
		
	}
	
}
